package day13_String;

public class javaStringMethod {

	public static void main(String[] args) {

		
		String str1 = "        Hello   ";
		System.out.println(str1.length());
		
		System.out.println(str1);
		
		
		str1 = str1.trim();
		
		System.out.println(str1.length());
		
		System.out.println("Rumplestiltskin".length());
	}

}
