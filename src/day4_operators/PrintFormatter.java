package day4_operators;

public class PrintFormatter {

	public static void main(String[] args) {

		// %s = String
		// $d = Digit(byte, short, int, long)
		// %f = Float (float, double)
		// 
		
	
		
	
		

		System.out.printf("Hello my name is %s", "Mohammed");
		
		System.out.println();
		
		System.out.printf("%s my name is %s", "Salam", "Mohammed");
		
		System.out.println();
		
		System.out.printf("Is it sunny today? %b", false);
		
		
		
		
	}

}
