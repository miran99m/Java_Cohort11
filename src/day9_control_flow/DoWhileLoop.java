package day9_control_flow;

public class DoWhileLoop {

	public static void main(String[] args) {

		int i = 0;

		do {
			System.out.println(i);
			i++;
		} while (i < 5);

		do {
			System.out.println("Hello Cohort 11");
		} while (false);

		int a = 10;

		do {
			System.out.println(a);
			a--;
		} while (a > 1);
		
		//-----------------------------------
		System.out.println("WHile Loop");
		
		
	}

}
