package day7_2ndReview;

public class RelationalDemo {

	public static void main(String[] args) {

		// Relational OPerator = Boolean Expression
		/*
		 * 1. >
		 * 2. <
		 * 3. == means equal
		 * 4. != means not equal
		 * 5. >=
		 * 6. <=
		 */
		System.out.println(10 != 4); //true
		
		// ==, != can use to compare primitive data type
		char ch1 = 'a';
		char ch2 = 'b';
		System.out.println(ch1 == ch2); //false
		
		
	}

}
